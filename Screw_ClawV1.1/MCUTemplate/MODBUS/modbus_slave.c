void ModBus_Pool(void)
{
	ModBusFailCode_Type errcode = MODBUS_OK;
	if(MState == MODBUS_TIMEOUT)
	{
		MState = MODBUS_HANDLING;	
		if (RecBuff[0] != MODBUS_ADDR)
		{
			MState = MODBUS_IDLE;
			return;
		}
#if SKIP_CRC==0
		if (!Check_CRC16(RecBuff, RecCount))
		{
			//errcode = MODBUS_ERR_CRC;
			//goto errorsend;
			MState = MODBUS_IDLE;
			return;
		}
#endif
		FUN_485_DIR(DIR_485_WRITEONLY);
		switch(RecBuff[1])
		{
			case 0x03:Handle_0x03();break;
			case 0x06:Handle_0x06();break;
			case 0x10:Handle_0x10();break;
			case 0x64:Handle_0x64();break;
		}
		goto end0;
errorsend:
		SendError(RecBuff[1], errcode);	
end0:	
		MState = MODBUS_SENDING;
		FUN_485_DIR(DIR_485_READONLY);
	}
}

