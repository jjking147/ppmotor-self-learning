static void Handle_0x03(void)
{
	u16 base_addr,len;
	Data8To16(base_addr,RecBuff[2],RecBuff[3]);
	Data8To16(len,RecBuff[4],RecBuff[5]);
	RecBuff[2] = 2*len;
	u8 region = base_addr / 100;
	u8 subaddr = base_addr % 100;
	if(!ValidateRegRange(region, subaddr + len))
	{
		SendError(0x03, MODBUS_ERR_Reg);
		return;
	}	
	if (region == 3)
		UpdateRawValue_Handler(subaddr, len);
	u16* p_reg = (u16*)Reg_Regions[region] + subaddr;
	u8* p_buff=&RecBuff[3];
	memcpy(p_buff, p_reg, 2*len);
	Attach_CRC16(RecBuff,3+RecBuff[2]);
	FUN_SEND_DATA(RecBuff,5+RecBuff[2]);
}

static void Handle_0x06(void)
{
	u16 base_addr,value;
	Data8To16(base_addr,RecBuff[2],RecBuff[3]);
	Data8To16(value,RecBuff[4],RecBuff[5]);
	u8 region = base_addr / 100;
	u8 subaddr = base_addr % 100;
	if (!ValidateRegRange(region, subaddr))
	{
		SendError(0x06, MODBUS_ERR_Reg);
		return;
	}
	u16* p_reg = (u16*)Reg_Regions[region] + subaddr;
	if (region == 5)
	{
		u8 rt = ExecuteCommand_Handler((u16*)Reg_Regions[5]);
		if (rt)
		{
			SendError(0x06, (ModBusFailCode_Type)rt);
			return;
		}
	}
	*p_reg = value;
	FUN_SEND_DATA(RecBuff,8);
}

static void Handle_0x10(void)
{
	u16 base_addr,len,len_byte;
	Data8To16(base_addr,RecBuff[2],RecBuff[3]);
	Data8To16(len,RecBuff[4],RecBuff[5]);
	len_byte = RecBuff[6];
	u8 region = base_addr / 100;
	u8 subaddr = base_addr % 100;
	if (!ValidateRegRange(region, subaddr + len))
	{
		SendError(0x10, MODBUS_ERR_Reg);
		return;
	}
	u16* p_reg = (u16*)Reg_Regions[region] + subaddr;
	u8* p_buff = &RecBuff[7];
	memcpy(p_reg, p_buff, len_byte);	
	if (region == 5)
	{
		u8 rt = ExecuteCommand_Handler((u16*)Reg_Regions[5]);
		if (rt)
		{
			SendError(0x10, (ModBusFailCode_Type)rt);
			return;
		}
	}
	Attach_CRC16(RecBuff,6);
	FUN_SEND_DATA(RecBuff,8);
}

