static void SendError(u8 fun, ModBusFailCode_Type errcode)
{
	u8 err[5] = {MODBUS_ADDR, fun | 0x80, errcode};
	Attach_CRC16(err, 3);
	FUN_SEND_DATA(err, 5);
}
