//计算CRC16
static uint16_t Get_CRC16( unsigned char * pucFrame, uint16_t usLen )
{
    unsigned char  ucCRCHi = 0xFF;
    unsigned char  ucCRCLo = 0xFF;
    u16 iIndex=0;

    while( usLen-- )
    {
        iIndex = ucCRCLo ^ *( pucFrame++ );
        ucCRCLo = ( unsigned char )( ucCRCHi ^ aucCRCHi[iIndex] );
        ucCRCHi = aucCRCLo[iIndex];
    }
	Data8To16(usLen,ucCRCHi,ucCRCLo);
    return usLen;
}

static void Attach_CRC16(unsigned char* pucFrame, uint16_t dataLen)
{
	 unsigned char  ucCRCHi = 0xFF;
    unsigned char  ucCRCLo = 0xFF;
	uint16_t usLen = dataLen;
    u16 iIndex=0;
	unsigned char* p=pucFrame;

    while( usLen-- )
    {
        iIndex = ucCRCLo ^ *( pucFrame++ );
        ucCRCLo = ( unsigned char )( ucCRCHi ^ aucCRCHi[iIndex] );
        ucCRCHi = aucCRCLo[iIndex];
    }
#if CRC_ORDER
	p[dataLen+1] = ucCRCHi;
	p[dataLen] = ucCRCLo;
#else
	p[dataLen] = ucCRCHi;
	p[dataLen+1] = ucCRCLo;
#endif
	
}

static unsigned char Check_CRC16(unsigned char* p,u16 count)
{
	u16 crc = 0;
#if CRC_ORDER
	Data8To16(crc,p[count-1], p[count-2]);
#else
	Data8To16(crc,p[count-2], p[count-1]);
#endif
	if(crc == Get_CRC16(p,count - 2))
		return 1;
	return 0;
}

void ModBusRecByte(u8 data)
{
	if(MState == MODBUS_IDLE)
	{
		RecBuff[0] = data;
		RecCount = 1;
		FUN_RESET_TIMER();
		FUN_START_TIMER();
		MState = MODBUS_RECING;
	}
	else if(MState == MODBUS_RECING)
	{
		RecBuff[RecCount++] = data;
		if (RecCount >= MODBUS_BUFF_LEN)
			RecCount = 0;
		FUN_RESET_TIMER();
	}
}

